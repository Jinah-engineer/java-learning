public class Excercise06 {

	public static void main(String[] args) {
		
		int lengthTop = 5;
		int lengthBottom = 10;
		int height = 7;
		double area = (lengthTop + lengthBottom) * height / 2.0; // double 타입의 결과를 얻기 위한 double 값 or double 타입 변환이 필요하다
		System.out.println(area);
	}

}
